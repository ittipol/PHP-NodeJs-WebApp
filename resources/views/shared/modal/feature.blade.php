<script type="text/javascript">

  class ModalFeature {

    constructor() {
      this.currentPage = 1;
      this.totalPage = 4;
      this.allowedClick = true;
      this.currentBg = null;
    }

    init() {
      // $('.modal-feature-wrapper').css('height',$(window).height() - 80);
      $('.modal-feature-content').css('min-height',$(window).height());
      this.bind();
    }

    bind() {

      let _this = this;

      $('#feature_page_next').on('click',function(){
        _this.next();
      });

      $('#feature_page_prev').on('click',function(){
        _this.prev();
      });
      
      $('#feature_modal_close').on('click',function(){
        _this.close();
      });

    }

    page(page) {

      if((this.currentPage < 1) ||  (this.currentPage > this.totalPage)) {
        return;
      }

      this.bg(page);

      $('.modal-feature-wrapper').addClass('show');

      $('#feature_'+page).addClass('show');

      $('#feature_page_progress').css('width',((page * 100) / this.totalPage)+'%');

      this.currentPage = page;

    }

    next() {

      if(!this.allowedClick) {
        return;
      }

      if(this.currentPage > (this.totalPage-1)) {
        return;
      }

      if(this.currentPage == (this.totalPage-1)) {
        $('#feature_page_next').addClass('dn').removeClass('dib');
      }else {
        $('#feature_page_prev').addClass('dib').removeClass('dn');
      }

      let _this = this;

      this.allowedClick = false;

      $('#feature_'+this.currentPage++).removeClass('show');

      this.bg(this.currentPage);

      $('#feature_'+this.currentPage).addClass('show');

      $('#feature_page_progress').css('width',((this.currentPage * 100) / this.totalPage)+'%');
    
      $('.modal-feature-wrapper').scrollTop(0);

      setTimeout(function(){
        _this.allowedClick = true;
      },400);
    }

    prev() {

      if(!this.allowedClick) {
        return;
      }

      if(this.currentPage < 2) {
        return;
      }

      if(this.currentPage == 2) {
        $('#feature_page_prev').addClass('dn').removeClass('dib');
      }else {
        $('#feature_page_next').addClass('dib').removeClass('dn');
      }

      let _this = this;

      this.allowedClick = false;

      $('#feature_'+this.currentPage--).removeClass('show');

      this.bg(this.currentPage);

      $('#feature_'+this.currentPage).addClass('show');

      $('#feature_page_progress').css('width',((this.currentPage * 100) / this.totalPage)+'%');
    
      $('.modal-feature-wrapper').scrollTop(0);

      setTimeout(function(){
        _this.allowedClick = true;
      },400);
    }

    show() {
      const modalDialog = new ModalDialog();
      modalDialog.show('#model_feature');
    }

    close() {
      const modalDialog = new ModalDialog();
      modalDialog.close();
    }

    bg(page) {

      let bg = 'feature-bg';

      switch(page) {

        case 1:
          bg = 'feature-bg-1';
        break;

        case 2:
          bg = 'feature-bg-1-1';
        break;

      }

      if(this.currentBg != null) {
        $('.feature-bg-drop').removeClass(this.currentBg);
      }

      $('.feature-bg-drop').addClass(bg);

      this.currentBg = bg;

    }

    clear() {
      this.currentPage = 1;

      $('#feature_page_prev').removeClass('dib').addClass('dn');
      $('#feature_page_next').removeClass('dib').removeClass('dn');

      $('.feature-bg-drop').removeClass('feature-bg');
      $('.modal-feature-content').removeClass('show');
      $('#feature_page_progress').css('width',0);
    }
  }

  const modalFeature = new ModalFeature();
  modalFeature.init();

</script>